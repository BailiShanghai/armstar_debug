/*
 * The RSA test data is generated by OpenSSL tool. More information about
 * OpenSSL please see https://www.openssl.org/
 *
 * And The following steps show how to generate test data:
 *
 * 1. Generate RSA keypair.
 *      openssl genrsa -out rsakey.pem 2048
 *    This command will generate one RSA 2048 keypair and save to rsakey.pem
 *    file. If want to generate RSA 4096 key, replace 2048 with 4096.
 *
 * 2. Dump keypair:
 *      openssl rsa -in rsakey.pem -text -noout
 *    Note: The "modulus" is the RSA public key N,
 *          the "prime1" is the RSA prime number P,
 *          the "prime2" is the RSA prime number Q,
 *          the "privateExponent" is the RSA private key D,
 *          the "publicExponent" is the RSA public key E.
 *    Note: There may exit 00 before the modulus data, prime1, and prime2,
 *          just remove it.
 *
 * 3. Generate some message data. Here is fixed string as message data:
 *      echo "message123" > message.bin
 *
 * 4. Generate hash.
 *      openssl dgst -sha256 -binary -out hash.bin message.bin
 *    This command will generate the SHA256 of message.bin and output to
 *    file hash.bin.
 *    If want to use SHA1, replace "-sha256" with "-sha1".
 *
 * for verify example:
 * 5. Generate signature:
 *      openssl pkeyutl -sign -pkeyopt rsa_padding_mode:pkcs1 \
 *          -pkeyopt digest:sha256 \
 *          -inkey rsakey.pem -in hash.bin -out sign.bin
 *    This command will generate the RSASSA_PKCS_V15 signature of hash.bin,
 *    with the RSA keypair "rsakey.pem".
 *    Note: The "-pkeyopt digest:sha256" indicates the message hash type.
 *          Change "sha256" with "sha1" for SHA1 type.
 *    Note: The "-pkeyopt rsa_padding_mode:pkcs1" indicates the RSA padding
 *          mode. Change "pkcs1" with "pss" for PKCS#1 v2.1 padding mode.
 *    Note: It is suggested to keep the hash algorithm aligned in generating
 *          hash and generating signature steps.
 *
 * 6. Dump signature:
 *      hexdump -C sign.bin
 */

